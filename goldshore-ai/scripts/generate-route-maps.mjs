#!/usr/bin/env node
import { promises as fs } from 'node:fs';
import path from 'node:path';

const ROOT = process.cwd();
const APPS_DIR = path.join(ROOT, 'apps');
const OUT_JSON = path.join(ROOT, 'docs/architecture/route-map.json');
const OUT_MD = path.join(ROOT, 'docs/architecture/ownership-matrix.md');

const METHOD_REGEX = /(\w+)\.(get|post|put|patch|delete|all)\(\s*['"`]([^'"`]+)['"`]/g;
const MOUNT_REGEX = /(\w+)\.route\(\s*['"`]([^'"`]+)['"`]\s*,\s*(\w+)\s*\)/g;
const HONO_VAR_REGEX = /(?:const|let|var)\s+(\w+)\s*=\s*new\s+Hono\b/g;

const toWebRoute = (relativeFile) => {
  const noExt = relativeFile.replace(/\.(astro|md|mdx|ts|js)$/, '');
  const parts = noExt.split(path.sep);
  const out = [];
  for (const part of parts) {
    if (part === 'index') continue;
    if (part.startsWith('[') && part.endsWith(']')) out.push(`:${part.slice(1, -1)}`);
    else out.push(part);
  }
  return '/' + out.join('/');
};

const walk = async (dir) => {
  const entries = await fs.readdir(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.name === 'node_modules' || entry.name === 'dist' || entry.name.startsWith('.')) continue;
    if (entry.isDirectory()) files.push(...(await walk(full)));
    else files.push(full);
  }
  return files;
};

const appDirs = (await fs.readdir(APPS_DIR, { withFileTypes: true }))
  .filter((d) => d.isDirectory())
  .map((d) => d.name)
  .sort();

const allRoutes = [];

for (const appName of appDirs) {
  const appRoot = path.join(APPS_DIR, appName);
  const srcDir = path.join(appRoot, 'src');
  try { await fs.access(srcDir); } catch { continue; }

  const files = await walk(srcDir);
  const routeDefs = [];
  const mounts = [];

  for (const file of files) {
    const rel = path.relative(ROOT, file);
    const relFromPages = rel.includes(`${path.sep}src${path.sep}pages${path.sep}`)
      ? rel.split(`${path.sep}src${path.sep}pages${path.sep}`)[1]
      : null;

    const deprecatedByFilename = file.includes('.legacy-');
    const tsSiblingExists = file.endsWith('.js') && await fs.access(file.replace(/\.js$/, '.ts')).then(() => true).catch(() => false);
    const status = (deprecatedByFilename || tsSiblingExists) ? 'deprecated' : 'active';

    if (relFromPages && /\.(astro|md|mdx)$/.test(file)) {
      const isApiRoute = relFromPages.startsWith(`api${path.sep}`);
      allRoutes.push({ kind: isApiRoute ? 'astro-api-handler' : 'frontend-page', owner: appName, method: isApiRoute ? 'ANY' : 'GET', path: toWebRoute(relFromPages), source: rel, status });
    }

    if (!/\.(ts|js)$/.test(file) || /\.test\.(ts|js)$/.test(file)) continue;
    const text = await fs.readFile(file, 'utf8');
    const honoVars = new Set(Array.from(text.matchAll(HONO_VAR_REGEX)).map((m) => m[1]));

    for (const match of text.matchAll(METHOD_REGEX)) {
      if (!honoVars.has(match[1])) continue;
      routeDefs.push({ varName: match[1], method: match[2].toUpperCase(), subPath: match[3], source: rel, status });
    }

    for (const match of text.matchAll(MOUNT_REGEX)) {
      mounts.push({ parentVar: match[1], mountPath: match[2], childVar: match[3], source: rel, status });
    }
  }

  const prefixesByVar = new Map([['app', ['']]]);
  let changed = true;
  while (changed) {
    changed = false;
    for (const m of mounts) {
      const parentPrefixes = prefixesByVar.get(m.parentVar) ?? [];
      if (!parentPrefixes.length) continue;
      const childPrefixes = prefixesByVar.get(m.childVar) ?? [];
      for (const parentPrefix of parentPrefixes) {
        const merged = `${parentPrefix}${m.mountPath}`.replace(/\/+/g, '/').replace(/\/$/, '') || '/';
        if (!childPrefixes.includes(merged)) {
          childPrefixes.push(merged);
          changed = true;
        }
      }
      prefixesByVar.set(m.childVar, childPrefixes);
    }
  }

  for (const def of routeDefs) {
    const prefixes = prefixesByVar.get(def.varName) ?? [''];
    for (const prefix of prefixes) {
      const routePath = `${prefix}${def.subPath}`.replace(/\/+/g, '/');
      allRoutes.push({ kind: 'worker-handler', owner: appName, method: def.method, path: routePath, source: def.source, status: def.status });
    }
  }
}

allRoutes.sort((a, b) => a.owner.localeCompare(b.owner) || a.path.localeCompare(b.path) || a.method.localeCompare(b.method));
await fs.mkdir(path.dirname(OUT_JSON), { recursive: true });
await fs.writeFile(OUT_JSON, JSON.stringify({ generatedAt: new Date().toISOString(), routes: allRoutes }, null, 2) + '\n');

let md = `# Route Ownership Matrix\n\nGenerated by \`node scripts/generate-route-maps.mjs\`.\n\n`;
md += `| Owner | Type | Method | Route | Source | Status |\n| --- | --- | --- | --- | --- | --- |\n`;
for (const route of allRoutes) md += `| ${route.owner} | ${route.kind} | ${route.method} | \`${route.path}\` | \`${route.source}\` | ${route.status} |\n`;

md += `\n## Duplicate Signature Scan\n\n`;
const sigMap = new Map();
for (const r of allRoutes.filter((x) => x.kind !== 'frontend-page' && x.status === 'active')) {
  const key = `${r.owner} ${r.method} ${r.path}`;
  const arr = sigMap.get(key) ?? [];
  arr.push(r.source);
  sigMap.set(key, arr);
}
const duplicates = [...sigMap.entries()].filter(([, v]) => v.length > 1);
if (!duplicates.length) md += `No duplicate API handler signatures found per owning app.\n`;
else for (const [sig, sources] of duplicates) md += `- **${sig}** appears in: ${sources.map((s) => `\`${s}\``).join(', ')}\n`;

await fs.writeFile(OUT_MD, md);
console.log(`Wrote ${path.relative(ROOT, OUT_JSON)} and ${path.relative(ROOT, OUT_MD)}`);
